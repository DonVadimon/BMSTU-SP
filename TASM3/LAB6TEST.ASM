MYCODE SEGMENT 'CODE'
    ASSUME CS:MYCODE, DS:DATA
	
START:
	;     DS
	MOV AX, DATA
	MOV ES, AX
	
MAIN:

	; CALL CLRSCR;
	MOV SI, 80H
	ADD SI, 2

	PUSH DX
	; STORE NUMS TO CX AND BX
	ZALOOP:
		MOV DL, [SI]
		INC SI
		CMP DL, 0DH
		JE ENDZALOOP
		CMP DL, 20H
		JE ZALOOP
		CMP DL, 40H
			JLE ISNUM
			JGE ISWORD

	ISNUM:
		SUB DL, 30H
		PUSH DX
		JMP ZALOOP
	
	ISWORD:
		SUB DL, 37H
		PUSH DX
		JMP ZALOOP

	ENDZALOOP:

	; FIRST NUM
	POP DX
	POP CX
	SHL CX, 4
	ADD CX, DX
	
	; SECOND NUM
	POP DX
	POP BX
	SHL BX, 4
	ADD BX, DX

	; --------OUTPUT--------
	CALL CLRF
	MOV AX, CX
	CALL HEX
	
	; SPACE
	MOV DL, ' '
	CALL PUTCH
	; PLUS
	MOV DL, '+'
	CALL PUTCH
	; SPACE
	MOV DL, ' '
	CALL PUTCH
	
	MOV AX, BX
	CALL HEX
	
	; SPACE
	MOV DL, ' '
	CALL PUTCH
	; EQUALS
	MOV DL, '='
	CALL PUTCH
	; SPACE
	MOV DL, ' '
	CALL PUTCH
	
	; COUNT SUM
	MOV AX, CX
	ADD AX, BX
	CALL HEX
	
	POP DX

EXIT:

	CALL GETCH
	
	CALL CLRSCR;
	
	;   
	MOV AL, 0
	MOV AH, 4CH
	INT 021H

	HEX PROC
		PUSH BX
		MOV BX, OFFSET HEXSTRING
		PUSH AX
		SHR AL, 4

		XLAT ES:[0]

  		MOV DL, AL
		CALL PUTCH
		POP AX
		AND AL, 00001111B

		XLAT ES:[0]

  		MOV DL, AL
		CALL PUTCH
		POP BX
  		RET
	HEX ENDP
	
	;  -    
	PUTST PROC
		MOV AH, 09H
		INT 021H
		RET
	PUTST ENDP
	
	;  -    
	PUTCH PROC
		MOV AH, 02H
		INT 021H
		RET
	PUTCH ENDP

	;  -  
	CLRF PROC
		MOV DL, 10
		CALL PUTCH
		MOV DL, 13
		CALL PUTCH
		RET
	CLRF ENDP 

	;  -  
	GETCH PROC   
		MOV AH, 08H
		INT 021H
        	RET
	GETCH ENDP

	;  -  
	CLRSCR PROC   
		MOV AH, 00H 
		MOV AL, 02
		INT 10H
		RET
	CLRSCR ENDP

	
;  
MYCODE ENDS

DATA SEGMENT
	HEXSTRING DB '0123456789ABCDEF' ;
DATA ENDS

END START
